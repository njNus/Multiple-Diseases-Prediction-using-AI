def main():
    # Create a navigation bar
    selected = option_menu(
        menu_title=None,
        options=["Home", "Menus"],
        icons=["house", "list-task"],
        orientation="horizontal",
    )

    if selected == "Home":
        show_homepage()
    elif selected == "Menus":
        show_disease_options()

def show_homepage():
    st.title("Multiple Disease Prediction System")
    st.write("Welcome to the Health Assistant! Please select a disease from the menu to get started.")

def show_disease_options():
    selected = option_menu(
        menu_title=None,
        options=["Diabetes Prediction", "Heart Disease Prediction", "Parkinsons Prediction"],
        icons=["activity", "heart", "person"],
        orientation="horizontal",
    )

    if selected == "Diabetes Prediction":
        diabetes_prediction_page()
    elif selected == "Heart Disease Prediction":
        heart_disease_prediction_page()
    elif selected == "Parkinsons Prediction":
        parkinsons_prediction_page()

